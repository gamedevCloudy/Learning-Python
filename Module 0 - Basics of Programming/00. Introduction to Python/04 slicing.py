#slicing = indexing a string (array of characters)

str = "Hey Jacob, let's go have some fun"

print(str[4]) #directly indexing an element
print('\n')
print(str[0:10]) #from 0 to 10
print('\n')
print(str[:5]) #everything b4 5th element
print('\n')
print(str[5:]) #everything after 5th element
print("\n")
print(str[-1])

print (len(str))